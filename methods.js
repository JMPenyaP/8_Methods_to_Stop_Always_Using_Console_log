// Muestra el número de veces que se ha llamado a un método
console.count();

// Un condicional, si es falso, muuestra un error
// console.assert(e!===0,"El divisor no puede ser cero");
console.assert();

// Muestra datos en forma de tabla
console.table();

// Mide el tiempo que tarda en ejecutarse una operación entre ambos métodos
console.time();
console.timeEnd();

// Para mostrar advertencias
console.warn();

// Para errores críticos
console.error();

// Muestra un listado de las propieddes de un objeto
/*Object
    name: "Dana"
    age: 20
[[Prototype]]:Object */
console.dir();

// Muestra la secuencia de llamadas a funciones hasta donde se ejecutó
/*Traza de la pila de llamadas:
functionB   @VM1117:8
functionA   @VM1117:4
(anonymous) @VM1117:11 */
console.trace();